#Задача 2 про метода случайного леса.
#В лесу растут деревья, среди которых 43% берез, 13% елей и 44% сосен. Для замеров деревьев случайным образом выбирается одно из них. 
#При замере диаметра деревьев он оказался больше 15 см для 30% берез, 40% елей и 70% сосен. Найти вероятность того, что диаметр случайно выбранного дерева больше 15 см.

from sklearn.ensemble import RandomForestClassifier

# Подготовка данных
X_train = [[0.43], [0.13], [0.44]]  # Проценты берез, елей и сосен с диаметром больше 15 см
y_train = [1, 1, 1]  # 1 - диаметр больше 15 см

# Создание и обучение модели случайного леса
clf = RandomForestClassifier(n_estimators=100, random_state=42)
clf.fit(X_train, y_train)

# Прогнозирование вероятности диаметра больше 15 см для новых данных
вероятность_березы = 0.43
вероятность_ели = 0.13
вероятность_сосны = 0.44

прогноз = clf.predict_proba([[вероятность_березы],
                             [вероятность_ели],
                             [вероятность_сосны]])

вероятность_диаметр_больше_15 = прогноз.mean()

print("Вероятность того, что диаметр случайно выбранного дерева больше 15 см:", вероятность_диаметр_больше_15)
